# Regex to find these specific words.
# We sort keys by length to match "عبدالله" before "علي"
ordered_keys = sorted(ARABIC_NAME_MAP.keys(), key=len, reverse=True)

# \b = word boundary. This is CRITICAL.
# It ensures "حسن" (Hassan) is matched, but not "احسن" (Ahsan).
NAME_RE = re.compile(
    r"\b(" + r"|".join(re.escape(k) for k in ordered_keys) + r")\b"
)


def normalize_arabic_names(text: str) -> str:
    """
    Finds common Arabic names and replaces them with their
    spoken Kurdish equivalent.
    """

    def _replace_name(match):
        # Find the word in our dictionary and return its value
        return ARABIC_NAME_MAP[match.group(1)]

    return NAME_RE.sub(_replace_name, text)